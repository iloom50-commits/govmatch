def seed_kosme_policy_funds(conn) -> dict:
    """knowledge_base에 2026년 중진공 정책자금 시드 데이터 삽입.

    중복 방지: 같은 content->question이 이미 있으면 UPDATE, 없으면 INSERT.
    """
    cur = conn.cursor()
    inserted = 0
    updated = 0
    errors = 0

    for fund in KOSME_FUNDS_2026:
        content = {
            "question": fund["question"],
            "answer": fund["answer"],
            "context": fund["context"],
            "fund_name": fund["name"],
            "keywords": fund["keywords"],
            "source_year": 2026,
            "source_org": "중소벤처기업진흥공단(중진공)",
        }

        # 같은 fund_name이 이미 있는지 확인
        cur.execute(
            """
            SELECT id FROM knowledge_base
            WHERE source = 'admin-manual'
              AND content->>'fund_name' = %s
            LIMIT 1
            """,
            (fund["name"],),
        )
        existing = cur.fetchone()

        try:
            if existing:
                cur.execute(
                    """
                    UPDATE knowledge_base
                    SET content = %s,
                        category = %s,
                        confidence = 0.95,
                        updated_at = CURRENT_TIMESTAMP
                    WHERE id = %s
                    """,
                    (json.dumps(content, ensure_ascii=False), fund["category"], existing["id"]),
                )
                updated += 1
            else:
                cur.execute(
                    """
                    INSERT INTO knowledge_base
                        (source, knowledge_type, category, content, confidence, use_count)
                    VALUES ('admin-manual', 'faq', %s, %s, 0.95, 0)
                    """,
                    (fund["category"], json.dumps(content, ensure_ascii=False)),
                )
                inserted += 1
        except Exception as e:
            logger.error(f"[kosme_seed] 오류 — {fund['name']}: {e}")
            errors += 1

    conn.commit()
    logger.info(f"[kosme_seed] 완료 — 신규 {inserted}건, 업데이트 {updated}건, 오류 {errors}건")
    return {"inserted": inserted, "updated": updated, "errors": errors, "total": len(KOSME_FUNDS_2026)}
