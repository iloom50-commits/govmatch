def seed_ambiguous_knowledge(db_conn) -> dict:
    """자금상담 애매한 케이스 시드 데이터 knowledge_base에 적재."""
    import json
    cur = db_conn.cursor()
    inserted, skipped = 0, 0

    for item in FUND_AMBIGUOUS_DATA:
        ktype = item["type"]
        cat = item["category"]
        content = item["content"]
        conf = item["confidence"]

        try:
            key_field = content.get("question") or content.get("wrong_info") or content.get("topic", "")
            cur.execute("""
                SELECT id FROM knowledge_base
                WHERE knowledge_type = %s AND category = %s
                  AND (content->>'question' = %s OR content->>'wrong_info' = %s OR content->>'topic' = %s)
            """, (ktype, cat, key_field, key_field, key_field))
            if cur.fetchone():
                skipped += 1
                continue
            cur.execute("""
                INSERT INTO knowledge_base (source, knowledge_type, category, content, confidence)
                VALUES (%s, %s, %s, %s::jsonb, %s)
            """, ("ambiguous_seed", ktype, cat, json.dumps(content, ensure_ascii=False), conf))
            db_conn.commit()
            inserted += 1
        except Exception as e:
            db_conn.rollback()
            print(f"[ambiguous_seed] 오류 ({cat}): {e}")

    return {"inserted": inserted, "skipped": skipped, "total": len(FUND_AMBIGUOUS_DATA)}
