# 이 단어가 있으면 예외 (지원사업일 가능성 → 보호)
SUPPORT_EXCEPTIONS = [
    "모집", "참여기업", "참여자", "신청", "접수", "공모", "지원사업", "지원금",
]


def is_non_support_title(title) -> bool:
    """제목이 비지원 공고면 True. 단, 지원공고 신호(EXCEPTIONS)가 있으면 False로 보호."""
    if not title:
        return False
    if any(exc in title for exc in SUPPORT_EXCEPTIONS):
        return False
    return any(pat in title for pat in NON_SUPPORT_TITLE_PATTERNS)
